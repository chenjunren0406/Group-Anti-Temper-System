package GASS.utils;

public class FileToSend {
	String path;
	String name;
	byte[] key;
	
	public FileToSend(String path, String name, byte[] key) {
		this.path = path;
		this.name = name;
		this.key = key;
	}
	
	public String getPath() {
		return path;
	}
	
	public String getName() {
		return name;
	}
	
	public byte[] getKey() {
		return key;
	}
}
